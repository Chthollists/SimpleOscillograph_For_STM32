;晶振6MHz 定时器中断使P1.0  500us取反一次。使用定时器方式二

ORG 0000H
	LJMP MAIN
ORG 000BH
	LJMP IT0P
ORG 001BH
	LJMP IT1P
	
ORG 0030H
MAIN: 	MOV SP, #60
		MOV TMOD, #22H
		MOV TH0,  #06H   ;10KHZ:CEH    5KHZ:9CH    1KHZ:06H
		MOV TL0,  #06H
		SETB ET0
		SETB TR0
		
		MOV TH1,  #9CH   ;10KHZ:CEH    5KHZ:9CH    1KHZ:06H
		MOV TL1,  #9CH
		SETB ET1
		SETB TR1
		
		SETB EA		
HERE: 	AJMP HERE
		
		
IT0P:	CPL P3.5
		RETI
		
IT1P:	CPL P3.4
		RETI
END